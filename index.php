function fetchNotice()
{
	include "include/dbconfig.php" ;
	$sql="SELECT * FROM `notice` WHERE `status`='ACTIVE' ORDER BY `id` DESC" ;
	$res=mysqli_query($conn,  $sql) ;
	if(mysqli_num_rows($res) > 0)
	{		
		while($row=mysqli_fetch_assoc($res))
		{
			$now = time(); // or your date as well
			$your_date = strtotime($row['date']);
			$datediff = $now - $your_date;
			$days=round($datediff / (60 * 60 * 24));
			
			if($days <= 30) 
			{
				echo '<a target="_blank" href="Notice/'.$row['content'].'"><li class="list-group-item"><font color="blue"><img src="new.gif">&nbsp;'.date('d/m/Y',strtotime($row['date'])).' '.$row['notice'].'</font></li></a>' ;
			}
			else{
				echo '<a target="_blank" href="Notice/'.$row['content'].'"><li class="list-group-item"><font color="blue">'.date('d/m/Y',strtotime($row['date'])).' '.$row['notice'].'</font></li></a>' ;
		
			}
		}
		
	}
	
